/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func isSameTree(p *TreeNode, q *TreeNode) bool {
	return traverse(p, q)
}

func traverse(p *TreeNode, q *TreeNode) bool {
	if p == nil && q == nil {
		return true
	} else if p == nil || q == nil {
		return false
	}
	res := p.Val == q.Val
	res = res && traverse(p.Left, q.Left)
	res = res && traverse(p.Right, q.Right)
	return res
}

